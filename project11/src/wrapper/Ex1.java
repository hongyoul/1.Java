package wrapper;

public class Ex1 {

	public static void main(String[] args) {

		// Byte, Short, Integer, Long 의 차이는 용량차이
		Byte byte1 = 1;
		Short short1 = 1;
		Integer integer = 1;
		Long long1 =1l; // long타입 값을 표현할 때는 'l'을 붙여야함

		
		// 실수타입, 크기의 차이 Float = 4byte, Double = 8byte(표현 값이 더 깊다.)
		Float float1 = 1.0f; // float타입 값을 표현할 때는 'f'을 붙여야함
		Double double1 = 1.0;
		
		
		Character character = 'a';
		Boolean boolean1 = true;
	}

}
